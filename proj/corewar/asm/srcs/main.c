#include "asm.h"

int				main(int ac, char **av)
{
	int			fd;

	if (ac < 2)
		return (0);
	fd = open(av[1], O_RDONLY);
	if (fd < 0)
		return (-1);
	compile(fd);
	close(fd);
	return (0);
}
